
package confeitaria.domain.entidades;

public class Prato {
    private int id_prato;
    private String nome;
    private float valor_prato;

    public int getId_prato() {
        return id_prato;
    }

    public void setId_prato(int id_prato) {
        this.id_prato = id_prato;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getValor_prato() {
        return valor_prato;
    }

    public void setValor_prato(float valor_prato) {
        this.valor_prato = valor_prato;
    }
    
    
    
}
